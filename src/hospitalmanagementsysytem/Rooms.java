        // TODO add your handling code here:

        if (lblId.getText() == null || txtRoomType.getSelectedItem() == null || txtRoomCapacity.getText() == null || txtRoomLocation.getSelectedItem() == null || txtRoomCharges.getText() == null || txtDescription.getText() == null) {
            JOptionPane.showMessageDialog(this, "Fill All Requared Information");
            JOptionPane.showMessageDialog(this, "Select From Table");
        } else {

            String rno = lblId.getText();
            String rtype = (String) txtRoomType.getSelectedItem();
            String rcapa = txtRoomCapacity.getText();
            String rlocation = (String) txtRoomLocation.getSelectedItem();
            String rcharge = txtRoomCharges.getText();
            String rdescrip = txtDescription.getText();
            String Status = "Avaliable";

            try {
                pst = con.prepareStatement("UPDATE  rooms set RoomType=?,RoomCapacity=?,RoomLocation=?,RoomCharge=?,RoomDescription=?,Statues=? where Room_ID=?");
                pst.setString(1, rtype);
                pst.setString(2, rcapa);
                pst.setString(3, rlocation);
                pst.setString(4, rcharge);
                pst.setString(5, rdescrip);
                pst.setString(6, Status);
                pst.setString(7, rno);
                pst.executeUpdate();

                JOptionPane.showMessageDialog(this, " Room successfully Updated");

                AutoID();
                txtRoomCapacity.setText("");
                txtRoomCharges.setText("");
                txtDescription.setText("");
                patient_table();

                btnAdd.setEnabled(true);

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, " Failed To Update ");
                Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

