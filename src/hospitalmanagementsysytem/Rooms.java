    public final void AutoID() {
        try {
            java.sql.Statement s = con.createStatement();
            rs = s.executeQuery("SELECT MAX(Room_ID) FROM rooms");
            rs.next();
            String maxPatientNo = rs.getString("MAX(Room_ID)");
            if (maxPatientNo == null) {
                lblId.setText("RM001");
            } else {
                int id = Integer.parseInt(maxPatientNo.substring(2));
                id++;
                String newPatientNo = String.format("RM%03d", id);
                lblId.setText(newPatientNo);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void patient_table() {
        try {
            pst = con.prepareStatement("SELECT * FROM rooms");
            rs = pst.executeQuery();
            java.sql.ResultSetMetaData Rsm = rs.getMetaData();
            int c;
            c = Rsm.getColumnCount();

            DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();

                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("Room_ID"));
                    v2.add(rs.getString("RoomType"));
                    v2.add(rs.getString("RoomCapacity"));
                    v2.add(rs.getString("RoomLocation"));
                    v2.add(rs.getString("RoomCharge"));
                    v2.add(rs.getString("RoomDescription"));
                }
                df.addRow(v2);

            }

        } catch (SQLException ex) {
            Logger.getLogger(Patient.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

